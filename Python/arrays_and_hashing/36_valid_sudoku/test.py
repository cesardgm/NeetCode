import unittest
from solution import Solution

class TestSolution(unittest.TestCase):
  def test_isValidSudoku(self) -> None:
    sol: Solution = Solution()

    good_board = [
      ["5","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","5",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","4","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ]

    bad_board = [
      ["8","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","5",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","4","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ]

    self.assertTrue(sol.isValidSudoku(good_board), "Test Case: Valid Sudoku Board")
    self.assertFalse(sol.isValidSudoku(bad_board), "Test Case: Invalid Sudoku Board")

if __name__ == '__main__':
  unittest.main()
